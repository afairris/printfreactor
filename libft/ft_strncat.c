#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*s;

	s = s1;
	while (*s)
	{
		s++;
	}
	while (*s2 && n-- > 0)
	{
		*s++ = *s2++;
	}
	*s = '\0';
	return (s1);
}
