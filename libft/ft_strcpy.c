#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	char	*d;

	i = 0;
	d = dst;
	while (*(src + i) != '\0')
	{
		*(d + i) = *(src + i);
		i++;
	}
	*(d + i) = '\0';
	return (dst);
}
